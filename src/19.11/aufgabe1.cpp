#include <iostream>

int main() {

    using namespace std;

    int n1, n2;

    cout << "Aufgabe 1: Addition von zwei Zahlen" << endl;

    cout << "Nummer 1: "; cin >> n1; // Fragt den Benutzer nach der ersten Zahl.
    cout << "Nummer 1: "; cin >> n2; // Fragt den Benutzer nach der zweiten Zahl.

    // Gibt die Summe der beiden Zahlen aus durch Addition dieser.
    cout << "Summe: " << (n1 + n2); 
    
}